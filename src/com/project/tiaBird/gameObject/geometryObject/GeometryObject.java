package com.project.tiaBird.gameObject.geometryObject;

import com.project.tiaBird.gameObject.GameObject;

public class GeometryObject extends GameObject {
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
